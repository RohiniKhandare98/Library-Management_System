    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // code for search
        String bookID = jTextField1.getText();
        String studentID = jTextField2.getText();
        try{
            Connection con = DBConnection.getConnection();
            
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from issue where bookID = '"+bookID+"' and studentID = '"+studentID+"'");
            
            if(rs.next()){
                jTextField3.setText(rs.getString(3));
                jTextField4.setText(rs.getString(4));
                jTextField1.setEditable(false);
                jTextField2.setEditable(false);
            }else{
                JOptionPane.showMessageDialog(null, "Book is not issued to Student");
                setVisible(false);
                new returnBook().setVisible(true);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Connection Error");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //code for return button
        String bookID = jTextField1.getText();
        String studentID = jTextField2.getText();
        
        try{
            Connection con = DBConnection.getConnection();
            
            Statement st = con.createStatement();
            st.executeQuery("update issue set returnBook = 'YES' where studentID = '"+studentID+"' and bookID = '"+bookID+"'");
            JOptionPane.showMessageDialog(null, "Book Successfully Returned");
            setVisible(false);
            new returnBook().setVisible(true);
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Connection Error");
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

